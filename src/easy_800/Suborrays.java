package easy_800;

import java.util.Scanner;

public class Suborrays {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int t=sc.nextInt();
        while (t-->0){
            int n=sc.nextInt();
            for (int i=1;i<=n;i++) System.out.println(i+" ");
            System.out.println();
        }
    }
}
