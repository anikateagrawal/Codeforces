package easy_800;

import java.util.Scanner;

public class Maximum_Multiple_Sum {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int t=sc.nextInt();
        while (t-->0){
            int n=sc.nextInt();
            if(n==3) System.out.println(3);
            else System.out.println(2);
        }
    }
}
