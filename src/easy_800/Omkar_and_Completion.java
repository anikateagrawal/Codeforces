package easy_800;

import java.util.Scanner;

public class Omkar_and_Completion {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int t=sc.nextInt();
        while (t-->0){
            int n=sc.nextInt();
            for(int i=0;i<n;i++) System.out.print(1+" ");
            System.out.println();
        }
    }
}
